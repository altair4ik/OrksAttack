function createBattleField(configMatrix) {
    configMatrix.forEach(function (item, index) {
        var y = index * 20;
        item.forEach(function (item, index) {
            var x = index * 20;
            if (item === 1){
                walls.push(new Wall([x, y]));
            } else if (item === 2) {
                enemies.push(new Enemy([x, y]));
            } else if (item === 3){
                player = new Player([x, y]);
            }
        })
    })
}

